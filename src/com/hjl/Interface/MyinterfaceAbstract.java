package com.hjl.Interface;
/*
在任何版本的java中。接口都能定义抽象方法
格式:
public abstract 返回值类型 方法名称(参数);

1.接口当中的抽象方法， 修饰符必须是两个固定的关键词，public abstract
2.这两个关键字修饰符，可以选择性省略。
3.方法的三要素，可以随意定义，
*/
public interface MyinterfaceAbstract {

    //抽象方法
    public abstract void mathodAbs1();
     abstract void mathodAbs2();
    public  void mathodAbs3();
     void mathodAbs4();

}
