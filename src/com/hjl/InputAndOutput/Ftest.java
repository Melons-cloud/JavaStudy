package com.hjl.InputAndOutput;

import javax.swing.*;
import java.awt.*;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;
import java.io.*;


public class Ftest extends JFrame {
    public static void main(String[] args) {
        Ftest thisClass = new Ftest();  //创建本类对象
        thisClass.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        //设置该窗体为显示状态
        thisClass.setVisible(true);
    }
    private static final long serialVersionUID=1L;
    private JPanel jContentPane =null;   //创建面板对象
    private JTextArea jTextArea=null;   //创建文本域
    private JButton openButton =null;   //创建按钮对象
    private JButton closeButton=null;   //创建按钮对象
    private JButton getOpenButton(){

        if (openButton==null){
            openButton=new JButton();
            openButton.setText("写入文件");   //修改按钮信息
            openButton.addActionListener(new ActionListener() {
                //按钮的单击事件
                @Override
                public void actionPerformed(ActionEvent e) {
                    //创建文件对象
                    File file =new File("word.txt");
                    try {
                        //创建FileWriter对象
                        FileWriter fileWriter = new FileWriter(file);
                        //获取文本域中文本
                        String s =jTextArea.getText();
                        //将信息写入磁盘文件
                        fileWriter.write(s);
                        //关闭流
                    }catch (Exception e1){
                        e1.printStackTrace();

                    }
                }
            });

        }
        return openButton;
    }
    private JButton getCloseButton(){

        if (closeButton==null){

            closeButton=new JButton();
            closeButton.setText("读取文件");   //修改按钮信息
            closeButton.addActionListener(new ActionListener() {

                //按钮的单击事件
                @Override
                public void actionPerformed(ActionEvent e) {
                    //创建文本对象
                    File file =new File("word.txt");
                    try {
                        //创建FileReader对象
                        FileReader in =new FileReader(file);
                        //创建char型数组
                        char byt[] =new char[1024];


                        try {
                            //将字节读入数据
                            int len =in.read(byt);
                            //设置文本域的显示信息
                            jTextArea.setText(new String(byt,0,len));
                            //关闭流
                            in.close();


                        } catch (IOException ex) {
                            ex.printStackTrace();
                        }

                    } catch (FileNotFoundException ex) {
                        ex.printStackTrace();
                    }
                }
            });

        }
        return closeButton;
    }
    public Ftest(){
        super();
        initialize();
    }
    private void initialize(){

        this.setSize(300,200);
        this.setContentPane(getJContentPane());
        this.setTitle("JFrame");
    }
    private JPanel getJContentPane(){

        if (jContentPane==null){

            jContentPane=new JPanel();
            jContentPane.setLayout(new BorderLayout());
            //jContentPane.add(getJextArea(),BorderLayout.CENTER);
            //jContentPane.add(getControlPanel(),BorderLayout.SOUTH);

        }
        return jContentPane;
    }















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































}
